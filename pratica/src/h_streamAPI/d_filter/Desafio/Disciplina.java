package h_streamAPI.d_filter.Desafio;

public class Disciplina {
	
	final String nome;
	final String area;
	final double nota;
	
	public Disciplina(String nome, String area, double d) {
		this.nome = nome;
		this.area = area;
		this.nota = d;
	}

	public String getNome() {
		return nome;
	}

	public String getArea() {
		return area;
	}

	public double getNota() {
		return nota;
	}
	
	
	
	

}
