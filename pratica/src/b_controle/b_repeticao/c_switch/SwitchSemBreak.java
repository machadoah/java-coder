package b_controle.b_repeticao.c_switch;

public class SwitchSemBreak {
	// Executa todos os case abaixo!

	public static void main(String[] args) {

		String faixa = "laranja";

		switch (faixa) {

		case "preta":
			System.out.println("Sei o Bassai-Dai...");

		case "marrom":
			System.out.println("Sei o Tekki Shodam");
			
		case "roxa":
			System.out.println("Sei Heian Godan");
			
		case "verde":
			System.out.println("Sei Heian Yodan");
		
		case "laranja":
			System.out.println("Sei Heian Sandan");
			
		case "vermelha":
			System.out.println("Sei Heian Nidan");
			
		case "amarela":
			System.out.println("Sei Heian Shodan");
		}

	}

}
