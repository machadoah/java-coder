package a_fundamentos.g_tipoString;

public class Prints {
	
	public static void main(String[] args) {
		
		// print 
		System.out.print("Bom"); // Não pula linha, depois de impresso!
		
		// println
		System.out.println(" dia"); // Pula a linha depois da execução!
		
		// printf
		System.out.printf("Mega-sena: %d %d %d %d %d %d \n",4,8,34,0,56,42);
		
		System.out.printf("Meu sario corresponde a R$ %.2f", 850.86998273);
		
	}

}
